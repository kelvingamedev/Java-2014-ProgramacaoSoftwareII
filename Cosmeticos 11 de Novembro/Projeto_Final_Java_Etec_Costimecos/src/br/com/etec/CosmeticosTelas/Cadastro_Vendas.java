    public Cadastro_Vendas() throws SQLException {
        initComponents();

        Conexao conecta = new Conexao(); // Cria uma classe de conexao com o BD
        if (conecta.getCon()) { // Inicia um conex

            String sql = "SELECT ProNome FROM produto"; // String de busca dos produtos
            PreparedStatement stm = conecta.con.prepareStatement(sql); // Prepara a query
            ResultSet rs2 = stm.executeQuery(); //  Executa a query
            if (!rs2.wasNull()) { // Caso o resultado nao seja nulo
                while (rs2.next()) {// Procura os registro no bd

                    jComboBoxProduto.addItem(rs2.getString(sql));

                    //System.out.println(rs2.getString(1));
                }
                conecta.fechar();
            }

        }

    }

