    private void jComboBoxProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxProdutoActionPerformed
        Conexao conecta = new Conexao();
        if (conecta.getCon()) {
            try {
                String sql = "SELECT ProQuant FROM produto WHERE ProNome ='"
                        + "" + jComboBoxProduto.getSelectedItem().toString() + "'";
                PreparedStatement stm = conecta.con.prepareStatement(sql);
                ResultSet rs = stm.executeQuery();
                while (rs.next()) {
                    jSpinnerQuantidade.setValue(rs.getString(1));
                }
            } catch (SQLException ex) {
                Logger.getLogger(Cadastro_Vendas.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

    }//GEN-LAST:event_jComboBoxProdutoActionPerformed

